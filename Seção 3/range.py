num = range(2, 10) # Cria sequência de 2 a 9
num2 = range(10) # Cria sequência de 0 a 9
num3 = range(-4, 5) # Cria sequência de -4 a 4
num4 = range(3, 19, 3) # Cria sequência de 3 a 18 ao passo 3
num5 = range(18, 4, -3) # Cria sequência de 18 a 3 ao passo 3 negativo

for numero in num:
    print(numero)

for numero2 in num2:
    print(numero2)

for numero3 in num3:
    print(numero3)

for numero4 in num4:
    print(numero4)

for numero5 in num5:
    print(numero5)
